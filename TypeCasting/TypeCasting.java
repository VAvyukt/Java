package JavaBasics.TypeCasting;

/**
 *
 * @author Nikhil.Agrawal
 */
public class TypeCasting {
    public static void main(String args[])
    {
        int a=10;
        float b=a;
        System.out.println(b);
        float c=10.5f;
        int d=(int)c;
        System.out.println(d);
        long e=10;
        int  f=(int)e;
        System.out.println(f);
        int g=10;
        long h=g;
        System.out.println(h);
        
      
    }
}